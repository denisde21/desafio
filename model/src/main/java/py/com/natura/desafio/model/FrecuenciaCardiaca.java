package py.com.natura.desafio.model;

public class FrecuenciaCardiaca {
	
	private String sexo;
	
	private Integer edadInicial;
	
	private Integer edadFinal;
	
	private Integer mal;
	
	private Integer normalInicial;
	
	private Integer normalFinal;
	
	private Integer bienInicial;
	
	private Integer bienFinal;
	
	private Integer excelente;

	public String getSexo() {
		return sexo;
	}

	public Integer getEdadInicial() {
		return edadInicial;
	}

	public void setEdadInicial(Integer edadInicial) {
		this.edadInicial = edadInicial;
	}

	public Integer getEdadFinal() {
		return edadFinal;
	}

	public void setEdadFinal(Integer edadFinal) {
		this.edadFinal = edadFinal;
	}

	public Integer getMal() {
		return mal;
	}

	public void setMal(Integer mal) {
		this.mal = mal;
	}

	public Integer getNormalInicial() {
		return normalInicial;
	}

	public void setNormalInicial(Integer normalInicial) {
		this.normalInicial = normalInicial;
	}

	public Integer getNormalFinal() {
		return normalFinal;
	}

	public void setNormalFinal(Integer normalFinal) {
		this.normalFinal = normalFinal;
	}

	public Integer getBienInicial() {
		return bienInicial;
	}

	public void setBienInicial(Integer bienInicial) {
		this.bienInicial = bienInicial;
	}

	public Integer getBienFinal() {
		return bienFinal;
	}

	public void setBienFinal(Integer bienFinal) {
		this.bienFinal = bienFinal;
	}

	public Integer getExcelente() {
		return excelente;
	}

	public void setExcelente(Integer excelente) {
		this.excelente = excelente;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}

}
